# Square pattern

for i in range(4):
    
    for j in range(4):
        print("# ", end="")
        
    print()
        
    
# Triangular pattern
for i in range(4):
    
    for j in range(i + 1):
        print("# ", end="")
        
    print()  \
        
    
# Inverted triangular pattern   
for i in range(4):
    
    for j in range(4 - i):
        print("# ", end="")
        
    print() 
   
   
   
    
 